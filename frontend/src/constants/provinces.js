/**
 * แปลง province id เป็น location object { th, en }
 */
export const getProvinceLocation = (provinceId) => {
  const p = TH_PROVINCES.find((x) => x.id === provinceId);
  return p ? { th: p.th, en: p.en } : { th: '', en: '' };
};

/**
 * หา province id จาก location (th หรือ en)
 * รองรับทั้ง location object และ string
 */
export const getProvinceIdFromLocation = (location) => {
  if (!location) return null;
  const locTh = (typeof location === 'object' ? location?.th : location) || '';
  const locEn = (typeof location === 'object' ? location?.en : '') || '';
  const searchStr = `${locTh} ${locEn}`.toLowerCase();

  const match = TH_PROVINCES.find((p) => {
    if (locTh && locTh.includes(p.th)) return true;
    if (locEn && locEn.toLowerCase().includes(p.en.toLowerCase())) return true;
    if (searchStr.includes(p.id.replace(/-/g, ' '))) return true;
    return false;
  });
  return match ? match.id : null;
};
